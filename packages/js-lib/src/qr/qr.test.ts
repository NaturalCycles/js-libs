function matrixToRows(
  content: string,
  mode: QrMode,
  ecl: QrErrorCorrectionLevel,
  typeNumber: number,
): string[] {
  const qr = createQrCode(content, { mode, ecl, typeNumber })
  const rows: string[] = []
  for (let r = 0; r < qr.size; r++) {
    let s = ''
    for (let c = 0; c < qr.size; c++) s += qr.isDark(r, c) ? '1' : '0'
    rows.push(s)
  }
  return rows
}

test.each(FIXTURES)(
  'matches reference for $mode "$content" ecl=$ecl type=$typeNumber',
  ({ content, mode, ecl, typeNumber, size, rows }) => {
    const qr = createQrCode(content, { mode, ecl, typeNumber })
    expect(qr.size).toBe(size)
    expect(matrixToRows(content, mode, ecl, typeNumber)).toEqual(rows)
  },
)

test('auto-detects the encoding mode', () => {
  // numeric is the most compact -> smallest matrix; byte the least
  const numeric = createQrCode('12345')
  const alnum = createQrCode('HELLO WORLD')
  const byte = createQrCode('Hello, world!')

  // same payload, forced byte mode is never smaller than the auto-detected mode
  expect(createQrCode('12345', { mode: 'numeric' }).size).toBe(numeric.size)
  expect(createQrCode('HELLO WORLD', { mode: 'alphanumeric' }).size).toBe(alnum.size)
  expect(byte.size).toBeGreaterThanOrEqual(21)
})

test('auto-selects the smallest fitting version, growing with payload', () => {
  expect(createQrCode('a').size).toBe(21) // v1
  const big = createQrCode('a'.repeat(300))
  expect(big.size).toBeGreaterThan(21)
  // higher ECC level needs more space (never less) for the same payload
  expect(createQrCode('a'.repeat(100), { ecl: 'H' }).size).toBeGreaterThanOrEqual(
    createQrCode('a'.repeat(100), { ecl: 'L' }).size,
  )
})

test('encodes UTF-8 in byte mode (unlike the original Latin-1 behavior)', () => {
  // '€' is 3 UTF-8 bytes (0xE2 0x82 0xAC); a multibyte payload must still produce a valid matrix
  const qr = createQrCode('Price: 5€ café 🎉')
  expect(qr.size).toBeGreaterThanOrEqual(21)
  // matrix is fully populated with booleans
  expect(
    qr.modules.every(row => row.length === qr.size && row.every(v => typeof v === 'boolean')),
  ).toBe(true)
})

test('finder patterns are present in all three corners', () => {
  const qr = createQrCode('HELLO')
  const n = qr.size
  // top-left finder: dark border ring around a 3x3 dark core
  for (const [r0, c0] of [
    [0, 0],
    [0, n - 7],
    [n - 7, 0],
  ] as [number, number][]) {
    expect(qr.isDark(r0, c0)).toBe(true)
    expect(qr.isDark(r0 + 1, c0 + 1)).toBe(false) // inner light ring
    expect(qr.isDark(r0 + 3, c0 + 3)).toBe(true) // dark core
  }
})

test('toSvg renders a scalable svg of the right pixel size', () => {
  const qr = createQrCode('HELLO', { ecl: 'M' })
  const svg = qr.toSvg({ scale: 8, border: 4 })
  const dimPx = (qr.size + 8) * 8
  expect(svg.startsWith('<svg')).toBe(true)
  expect(svg).toContain(`width="${dimPx}"`)
  expect(svg).toContain(`viewBox="0 0 ${qr.size + 8} ${qr.size + 8}"`)
  expect(svg).toContain('<path d="M')
  expect(svg.endsWith('</svg>')).toBe(true)
})

test('toDataUrl produces a usable svg data url', () => {
  const url = createQrCode('HELLO').toDataUrl()
  expect(url.startsWith('data:image/svg+xml,')).toBe(true)
  expect(decodeURIComponent(url.slice('data:image/svg+xml,'.length)).startsWith('<svg')).toBe(true)
})

test('toAscii renders a bordered block-character grid', () => {
  const qr = createQrCode('HELLO')
  const ascii = qr.toAscii({ border: 2 })
  const lines = ascii.split('\n')
  // height = size + 2*border rows; width = (size + 2*border) * 2 chars (2 chars per module)
  expect(lines).toHaveLength(qr.size + 4)
  expect(lines[0]).toHaveLength((qr.size + 4) * 2)
  expect(ascii).toContain('██')
  // toString() is the default ascii rendering
  expect(qr.toString()).toBe(qr.toAscii())
})

test('accepts raw bytes directly', () => {
  const bytes = new TextEncoder().encode('HELLO')
  const fromBytes = createQrCode(bytes, { ecl: 'M' })
  const fromString = createQrCode('HELLO', { mode: 'byte', ecl: 'M' })
  expect(matrixToRows('HELLO', 'byte', 'M', 0)).toEqual(matrixToRows('HELLO', 'byte', 'M', 0))
  expect(fromBytes.size).toBe(fromString.size)
  // identical matrices
  for (let r = 0; r < fromBytes.size; r++) {
    for (let c = 0; c < fromBytes.size; c++) {
      expect(fromBytes.isDark(r, c)).toBe(fromString.isDark(r, c))
    }
  }
})

